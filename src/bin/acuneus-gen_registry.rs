use std::collections::BTreeMap;
use std::env;
use std::fs;
use std::io;
use std::path::{Path, PathBuf};

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
enum ParamType {
    F32,
    Color3,
    Action,
    String,
}

#[derive(Clone, Debug)]
struct Param {
    name: String,
    label: String,
    group: String,
    param_type: ParamType,
    min: f32,
    max: f32,
    default: f32,
}

#[derive(Clone, Debug)]
struct Field {
    name: String,
    ty: String,
}

#[derive(Clone, Debug)]
struct Example {
    name: String,
    source_file: String,
    title: String,
    default_width: u32,
    default_height: u32,
    params: Vec<Param>,
}

fn main() -> io::Result<()> {
    let mut write = false;
    let mut root = PathBuf::from(".");
    for arg in env::args().skip(1) {
        match arg.as_str() {
            "--write" => write = true,
            "--help" | "-h" => {
                eprintln!(
                    "usage: cargo run --bin acuneus-gen_registry -- [--write] [path-to-cuneus]"
                );
                return Ok(());
            }
            value => root = PathBuf::from(value),
        }
    }

    let examples = discover_examples(&root.join("examples"))?;
    let registry = render_registry(&examples);
    let cmake_examples = render_cmake_examples(&examples);
    let embedded = render_embedded(&examples);

    if write {
        fs::write(root.join("src/bin_registry.rs"), registry)?;
        fs::write(root.join("src/embedded_generated.rs"), embedded)?;
        let generated_dir = root.join("examples/generated");
        fs::create_dir_all(&generated_dir)?;
        fs::write(generated_dir.join("cuneus_examples.cmake"), cmake_examples)?;
    } else {
        println!("{registry}");
    }

    Ok(())
}

fn render_cmake_examples(examples: &[Example]) -> String {
    let mut out = String::new();
    out.push_str("# Generated by `cargo run --bin acuneus-gen_registry -- --write`.\n");
    out.push_str("set(BESPOKE_ACUNEUS_EXAMPLES\n");
    for example in examples {
        out.push_str(&format!("    {}\n", example.name));
    }
    out.push_str(")\n");
    out
}

fn discover_examples(examples_dir: &Path) -> io::Result<Vec<Example>> {
    let mut examples: Vec<Example> = Vec::new();
    for entry in fs::read_dir(examples_dir)? {
        let entry = entry?;
        let path = entry.path();
        if path.extension().and_then(|ext| ext.to_str()) != Some("rs") {
            continue;
        }

        let source_file = path
            .file_name()
            .and_then(|name| name.to_str())
            .unwrap_or_default()
            .to_string();
        let source_name = path
            .file_stem()
            .and_then(|stem| stem.to_str())
            .unwrap_or_default()
            .to_string();
        let name = source_name
            .strip_prefix("cuneus-")
            .unwrap_or(&source_name)
            .to_string();
        let source = fs::read_to_string(&path)?;
        let uniform_params = parse_uniform_params(&source);
        let fields = uniform_params
            .as_ref()
            .map(|(_, fields)| fields.as_slice())
            .unwrap_or(&[]);
        let defaults = uniform_params
            .as_ref()
            .map(|(params_ty, _)| parse_initial_defaults(&source, params_ty))
            .unwrap_or_default();
        let group_markers = parse_group_markers(&source);
        let slider_specs = parse_slider_specs(&source, &group_markers);
        let color_labels = parse_color_labels(&source);

        let mut params = Vec::new();
        for field in fields {
            if is_padding(&field.name) {
                continue;
            }

            let param_type = match field.ty.as_str() {
                "f32" => ParamType::F32,
                "[f32; 3]" | "[f32;3]" => ParamType::Color3,
                _ => continue,
            };

            let slider = slider_specs.get(&field.name);
            let label = slider
                .and_then(|spec| spec.label.clone())
                .or_else(|| color_labels.get(&field.name).cloned())
                .unwrap_or_else(|| title_case(&field.name));
            let group = slider
                .map(|spec| spec.group.clone())
                .filter(|group| !group.is_empty())
                .unwrap_or_else(|| "Parameters".to_string());
            let (min, max) = slider.map_or(default_range(param_type), |spec| (spec.min, spec.max));
            let default = defaults
                .get(&field.name)
                .copied()
                .unwrap_or_else(|| default_value(param_type));

            params.push(Param {
                name: field.name.clone(),
                label,
                group,
                param_type,
                min,
                max,
                default,
            });
        }

        let (default_width, default_height) = parse_shader_app_size(&source).unwrap_or((800, 600));
        let title = parse_shader_app_title(&source).unwrap_or_else(|| title_case(&name));
        append_builtin_controls(&source, &mut params);

        let example = Example {
            name,
            source_file,
            title,
            default_width,
            default_height,
            params,
        };
        if let Some(existing) = examples.iter_mut().find(|item| item.name == example.name) {
            if example.source_file.starts_with("cuneus-")
                && !existing.source_file.starts_with("cuneus-")
            {
                *existing = example;
            }
        } else {
            examples.push(example);
        }
    }

    examples.sort_by(|a, b| sort_name(&a.name).cmp(&sort_name(&b.name)));
    Ok(examples)
}

fn render_embedded(examples: &[Example]) -> String {
    let launchable: Vec<&Example> = examples.iter().collect();

    let mut out = String::new();
    out.push_str("// Generated by `cargo run --bin acuneus-gen_registry -- --write`.\n\n");
    out.push_str("pub fn can_run_bin(bin_name: &str) -> bool {\n");
    out.push_str("    matches!(\n");
    out.push_str("        bin_name,\n");
    for (index, example) in launchable.iter().enumerate() {
        if index > 0 {
            out.push_str(" |");
        }
        out.push_str(&format!(" \"{}\"", example.name));
    }
    out.push_str("\n    )\n");
    out.push_str("}\n\n");

    out.push_str("pub fn run_bin(bin_name: &str) -> Result<(), Box<dyn std::error::Error>> {\n");
    out.push_str("    match bin_name {\n");
    for example in &launchable {
        out.push_str(&format!(
            "        \"{}\" => {}::run(),\n",
            example.name,
            module_name(&example.name)
        ));
    }
    out.push_str(
        "        _ => Err(format!(\"embedded cuneus bin is not available: {bin_name}\").into()),\n",
    );
    out.push_str("    }\n");
    out.push_str("}\n\n");

    for example in &launchable {
        out.push_str(&format!("mod {} {{\n", module_name(&example.name)));
        out.push_str(&format!(
            "    include!(\"../examples/{}\");\n\n",
            example.source_file
        ));
        out.push_str("    pub fn run() -> Result<(), Box<dyn std::error::Error>> {\n");
        out.push_str("        main()\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }

    out
}

fn parse_uniform_params(source: &str) -> Option<(String, Vec<Field>)> {
    let macro_start = source.find("uniform_params!")?;
    let source = &source[macro_start..];
    let struct_pos = source.find("struct ")? + "struct ".len();
    let name_end = source[struct_pos..]
        .find(|ch: char| !(ch.is_ascii_alphanumeric() || ch == '_'))?
        + struct_pos;
    let params_ty = source[struct_pos..name_end].trim().to_string();
    let open = source[name_end..].find('{')? + name_end;
    let close = matching_brace(source, open)?;
    let body = &source[open + 1..close];
    let mut fields = Vec::new();

    for item in body.split(',') {
        let item = strip_line_comment(item).trim();
        if item.is_empty() {
            continue;
        }
        let Some((left, right)) = item.split_once(':') else {
            continue;
        };
        let name = left.split_whitespace().last().unwrap_or_default().trim();
        let ty = right.trim();
        if !name.is_empty() && !ty.is_empty() {
            fields.push(Field {
                name: name.to_string(),
                ty: ty.to_string(),
            });
        }
    }

    Some((params_ty, fields))
}

fn parse_shader_app_size(source: &str) -> Option<(u32, u32)> {
    let start = source.find("ShaderApp::new(")?;
    let args_start = start + "ShaderApp::new(".len();
    let args_end = source[args_start..].find(");")? + args_start;
    let args = &source[args_start..args_end];
    let numeric_args: Vec<u32> = args
        .split(',')
        .filter_map(|arg| arg.trim().parse::<u32>().ok())
        .collect();
    if numeric_args.len() < 2 {
        return None;
    }
    Some((
        numeric_args[numeric_args.len() - 2],
        numeric_args[numeric_args.len() - 1],
    ))
}

fn parse_shader_app_title(source: &str) -> Option<String> {
    let start = source.find("ShaderApp::new(")?;
    let args_start = start + "ShaderApp::new(".len();
    let args = &source[args_start..];
    let title_start = args.find('"')? + 1;
    let title_tail = &args[title_start..];
    let title_end = title_tail.find('"')?;
    Some(title_tail[..title_end].to_string())
}

fn parse_initial_defaults(source: &str, params_ty: &str) -> BTreeMap<String, f32> {
    let mut defaults = BTreeMap::new();
    let Some(initial_pos) = source.find("initial_params") else {
        return defaults;
    };
    let source = &source[initial_pos..];
    let needle = format!("{params_ty} {{");
    let Some(start) = source.find(&needle) else {
        return defaults;
    };
    let open = start + needle.len() - 1;
    let Some(close) = matching_brace(source, open) else {
        return defaults;
    };
    let body = &source[open + 1..close];

    for item in body.split(',') {
        let item = strip_line_comment(item).trim();
        let Some((name, value)) = item.split_once(':') else {
            continue;
        };
        let name = name.trim().to_string();
        if let Some(value) = parse_first_f32(value) {
            defaults.insert(name, value);
        }
    }

    defaults
}

#[derive(Clone, Debug)]
struct SliderSpec {
    min: f32,
    max: f32,
    label: Option<String>,
    group: String,
}

fn parse_slider_specs(
    source: &str,
    group_markers: &[(usize, String)],
) -> BTreeMap<String, SliderSpec> {
    let mut specs = BTreeMap::new();
    let mut rest = source;
    let mut offset = 0usize;
    while let Some(pos) = rest.find("Slider::new(&mut params.") {
        let absolute_pos = offset + pos;
        rest = &rest[pos + "Slider::new(&mut params.".len()..];
        offset = absolute_pos + "Slider::new(&mut params.".len();
        let Some(field_end) = rest.find(',') else {
            break;
        };
        let field = rest[..field_end].trim().to_string();
        let tail = &rest[field_end + 1..];
        let Some(close) = tail.find(')') else {
            break;
        };
        let range = &tail[..close];
        let Some((min, max)) = parse_range(range) else {
            rest = tail;
            continue;
        };
        let label = tail[close..].find(".text(\"").and_then(|label_start| {
            let label_tail = &tail[close + label_start + ".text(\"".len()..];
            label_tail
                .find('"')
                .map(|end| label_tail[..end].to_string())
        });

        specs.insert(
            field,
            SliderSpec {
                min,
                max,
                label,
                group: group_for_position(group_markers, absolute_pos),
            },
        );
        rest = tail;
        offset += field_end + 1;
    }
    specs
}

fn parse_group_markers(source: &str) -> Vec<(usize, String)> {
    let mut markers = Vec::new();
    for needle in ["CollapsingHeader::new(\"", "ui.collapsing(\""] {
        let mut rest = source;
        let mut offset = 0usize;
        while let Some(pos) = rest.find(needle) {
            let absolute_pos = offset + pos;
            let label_start = pos + needle.len();
            let label_tail = &rest[label_start..];
            if let Some(label_end) = label_tail.find('"') {
                markers.push((absolute_pos, label_tail[..label_end].to_string()));
            }
            rest = &rest[label_start..];
            offset = absolute_pos + needle.len();
        }
    }
    if let Some(pos) = source.find("render_media_panel(") {
        markers.push((pos, "Media".to_string()));
    }
    if let Some(pos) = source.find("render_controls_widget(") {
        markers.push((pos, "Transport".to_string()));
    }
    markers.sort_by_key(|(pos, _)| *pos);
    markers
}

fn group_for_position(markers: &[(usize, String)], pos: usize) -> String {
    markers
        .iter()
        .take_while(|(marker_pos, _)| *marker_pos <= pos)
        .last()
        .map(|(_, label)| label.clone())
        .unwrap_or_else(|| "Parameters".to_string())
}

